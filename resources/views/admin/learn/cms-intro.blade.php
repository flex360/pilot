        <h5>Create a Post</h5>

        <p>You can create your own news posts within the module and customize them. The editor works very similar to the pages module, however you also have "tags" to organize the content and can upload a featured image and a photo gallery to each post.</p>

        <h3>Users</h3>

        <p>It's always important to be mindful of who has access to your website's CMS. </p>

        <p><strong>Please note,</strong> if you would like FLEX360 to be able to edit content for you, do not delete or change the password for the user named "Admin". If you delete this user and lose your password, we will not be able to re-set it for you. (We will never change your website without your request/permission.)</p>

        <h4>Roles</h4>

        <p>Roles determine a users's permissions.&nbsp;</p>

        <h5>Administrators</h5>

        <p>Website administrators have full power to manage your website. They can add, edit, delete all page, posts, users, etc.</p>

        <h5>Editors</h5>

        <p>Website editors can add new pages or posts and edit current ones, but they cannot fully delete a post or a page. (They <em>can</em> delete the content of a page or a post.)</p>

