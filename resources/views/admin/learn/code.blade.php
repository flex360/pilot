        <h3>Block 1</h3>

        <p>Any code entered into Block 1 will display before any content you entered on the content tab.&nbsp;</p>

        <h3>Block 2</h3>

        <p>Any code entered into Block 2 will display after any content you entered on the content tab.&nbsp;</p>

        <p>If you have code in both code blocks, if you have anything entered in the content tab will display between the coded content.</p>

