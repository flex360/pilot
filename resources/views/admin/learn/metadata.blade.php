        <h3>Path</h3>

        <p>The path is made up of the Slug of the page plus any parent pages (if anything other than "home" -- your homepage lives at your domain name). The path is anything that shows up after the domain name. For example: flex360.com<strong>/learn/<u>metadata</u></strong></p>

        <p>The path is the specific address to a page after getting to the homepage. So if you created a hidden page for a marketing campaign, just go to the Metadata tab to find out what the path is. Then when you're sharing that link, it's just your [domain.com] + [path].</p>

        <h4>Slug</h4>

        <p>The slug is the part that identifies a particular page. From the above example /learn is the slug to this section's parent page and /metadata is the slug of this specific page. So if you wanted to link to this page from somewhere in this site, you'd just paste or type "/learn/metadata" where the "https://" is in the link tool.</p>

        <h3>Meta Title</h3>

        <p>When we built your site, we set it up to where the Meta Title is automatically set to be your page name. To futher improve your search engine optimizaton, you can customize each page's meta title.&nbsp;</p>

        <h3>Meta Description</h3>

        <p>We also built your site to automatically pull the first 120 characters from your content body as your meta description. To futher improve your search engine optimizaton, you can customize this information. Customizing the meta description is especially helpful if you start off the page with content that relies on formatting (such as bullets or numbered list) for punctuation. &nbsp;</p>

        <h3>Breadcrumb</h3>

        <p>Breadcrumb is a type of secondary navigation scheme that reveals the user's location in a website or Web application. Because so many people visit websites through mobile devices and displaying all the breadcrumb navigation can clutter up the screen, we've moved away from using this tool.</p>

