        <h2>Page Settings Tab</h2>

        <p><img alt="Image title" class="fr-fin fr-dib" src="/pilot-assets/legacy/learn/20170622141237-page-settingspng.png" width="100%"></p>

        <h3>Status</h3>

        <p>There are three options on a page status: Publish, Draft, Hidden.</p>

        <h4>Publish</h4>

        <p>A published page can be seen by any website visitor and a link to it will be added to your website navigation.</p>

        <h4>Draft</h4>

        <p>A draft page is something that only a website administrator or editor can see when logged in through the website Pilot. This is a great tool to use if you're working on some page content, but not ready for it to "go live" yet.</p>

        <h4>Hidden</h4>

        <p>A hidden page is technically "live" on the website, but no one can get to the page without the direct URL (domain.com/sug). Hidden pages are great for confirmation pages to let someone know their form was recieved. </p>

        <p>Hidden pages are also perfect for marketing campaigns because, when you look at your Google Analytics, you'll know that anyone who started off on your website through that page came to it because of your campaign - and then you can watch their progression through your site from there.</p>

        <h3>Layout</h3>

        <p>Layout refers to the page style sheet. "Master" is the homepage - so if you create a new page and the layout is set to Master, it'll just look like the homepage refreshed. So when you create a new page, make sure the Layout is set to "Internal".</p>

        <h3>Parent</h3>

        <p>A "Parent" page has to do with how the pages are organized. Group like "child" pages under a parent page.&nbsp;</p>

        <p>Note: please talk to your design/development team before making a second-level parent page that's public. Since so much web traffic is &nbsp;through mobile devices and multi-level drop-down menus are really difficult and annoying to operate on a mobile device, we do not design menus to go a second level unless that's something we've discussed with you specifically.&nbsp;</p>

        <h3>Redirect</h3>

        <p>Redirects are really helpful if you want to have something in your navigation, but direct that link elsewhere.&nbsp;</p>

        <p>Just like when you're creating a link, if you're linking to an internal page,all you have to paste in the "http://" area is the path. (Check out the&nbsp;<a href="/learn/metadata">Metadata</a>&nbsp;tip page to learn more about paths.)</p>

        <p><strong>Tip: It's not a good idea to redirect one of your pages directly to another website</strong> unless your web design/development team has built in tools specifically for that. Reason being, when someone clicks on a page that's been redirected, it'll just go there in the same window. If you're sending someone to another site, you've sent them away from your website and it's less easy for them to get back to you.</p>

