        <h6>Format Block: Headings (Heading 6)</h6>

        <p>Throughout this page, you'll see that there are styled section headings and in parentheses next to them, I have a heading number so you can know that that looks like.</p>

        <p>There are a few tips for headings that, if you follow, it will boost your search engine optimization (SEO):</p>

        <ul>
          <li>Headings make it easier for website visitors to read longer content pages.&nbsp;</li>

          <li>Use keywords or phrases in your headings, but make it readable.</li>

          <li>Don't use Heading 1. Your page title is automatically formatted as Heading 1, which indicates that everything else on the page is about whatever the title is.</li>

          <li>If you're going to use headings, start with Heading 2.</li>

          <li>If you use any heading 2-6 more than once on a page, do so to indicate the same level of heirarchy.&nbsp;</li>
        </ul>

