        <h5>Creating Links</h5>

        <p>Here's an <a href="https://www.flex360.com/" target="_blank">example link</a>. To create a link:</p>

        <ol>
          <li> Type the words you want linked,</li>

          <li>Highlight those words (select them) with your mouse or using "shift" and arrow keys,</li>

          <li>Click on the link icon and past the desired URL where you see "http://"</li>

          <li>If you're linking to someone else's website, check the "open in a new tab" box.</li>
        </ol>

